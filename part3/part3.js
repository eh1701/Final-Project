//arc de triomphe
var arcTextbox = document.getElementById('arcTextbox');
var arcImg = document.getElementById('arcImg');
var arc01 = document.getElementById("arc01");
var arcText = document.getElementById("arcText");
arcImg.onclick = function(){
    arcTextbox.style.display = "block";
    arc01.src = this.src;
    arcText.innerHTML = this.alt;
}
var span = document.getElementsByClassName("arcClose")[0];
span.onclick = function() {
    arcTextbox.style.display = "none";
}

